#include <iostream>

using namespace std;

int main (){
	int angka;
	cin>>angka;
	
	if (angka ==1 ){
		cout<<"satu\n";
	}
	else if (angka ==2 ){
		cout<<"dua\n";
	}
	else if (angka ==3 ){
		cout<<"tiga\n";
	}
	else if (angka >3) {
		cout<<"Bilangan Lebih Besar Dari Tiga\n";
	}
	
	return 0;
	
}
